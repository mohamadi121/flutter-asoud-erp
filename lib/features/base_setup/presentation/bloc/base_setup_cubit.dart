import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';

import '../../domain/entities/accounting_setup.dart';
import '../../domain/repositories/setup_repository.dart';

part 'base_setup_state.dart';

class BaseSetupCubit extends Cubit<BaseSetupState> {
  BaseSetupCubit(this._repository, this._company, {bool autoGenerateDetailCodes = true})
      : super(BaseSetupState(autoGenerateDetailCodes: autoGenerateDetailCodes));

  final SetupRepository _repository;
  final String _company;

  void setMoneyUnit(MoneyUnit value) => emit(state.copyWith(moneyUnit: value));
  void setFiscalMonth(int value) => emit(state.copyWith(fiscalYearStartMonth: value));
  void setChartTemplate(ChartTemplate value) => emit(state.copyWith(chartTemplate: value));
  void setAutoDetailCodes(bool value) => emit(state.copyWith(autoGenerateDetailCodes: value));
  Future<void> submit() async {
    if (state.status == BaseSetupStatus.saving || state.status == BaseSetupStatus.success) return;
    emit(state.copyWith(status: BaseSetupStatus.saving, clearError: true));
    try {
      await _repository.saveAccounting(
        _company,
        AccountingSetup(
          basis: AccountingBasis.accrual,
          moneyUnit: state.moneyUnit,
          fiscalYearStartMonth: state.fiscalYearStartMonth,
          chartTemplate: state.chartTemplate,
          autoGenerateDetailCodes: state.autoGenerateDetailCodes,
        ),
      );
      emit(state.copyWith(status: BaseSetupStatus.success));
    } catch (error) {
      emit(state.copyWith(status: BaseSetupStatus.failure, errorMessage: 'خطای سرور: $error'));
    }
  }
}
